    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }

        entitet = lstEntiteti.getSelectedValue();
        if (entitet == null) {
            return;
        }
        ocistiPolja();
        txtFirstName.setText(entitet.getFirstname());
        txtLastName.setText(entitet.getLastname());
        txtPosition.setText(entitet.getPosition());
        txtYerseyNumber.setText(entitet.getJerseynumber());
        
    }//GEN-LAST:event_lstEntitetiValueChanged

    private void btnDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajNoviActionPerformed

        entitet = new Player();

        if (!popuniSvojstva()) {
            return;
        }

        ocistiBoje();

        try {

            Player newOne = obrada.dodaj(entitet);

            ucitaj();

            for (int i = 0; i < lstEntiteti.getModel().getSize(); i++) {
                if (lstEntiteti.getModel().getElementAt(i).getIdNba()
                        == newOne.getIdNba()) {
                    lstEntiteti.setSelectedIndex(i);
                    lstEntiteti.ensureIndexIsVisible(lstEntiteti.getSelectedIndex());
                    break;
                }
            }

        } catch (NbaException ex) {
            switch (ex.getPoruka()) {
                case "Name required":
                    txtFirstName.setBackground(Color.RED);
                    break;
            }
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnDodajNoviActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        entitet = lstEntiteti.getSelectedValue();
        if (entitet == null) {
            JOptionPane.showMessageDialog(getRootPane(), "First chose a Player");
            return;
        }
        ocistiBoje();

        if (!popuniSvojstva()) {
            return;
        }

        try {
            obrada.promjena(entitet);
            ucitaj();
        } catch (NbaException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }

    }//GEN-LAST:event_btnPromjenaActionPerformed

